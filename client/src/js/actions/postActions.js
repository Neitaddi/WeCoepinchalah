import axios from "axios";
import {
  GET_POSTES,
  LIKE_POST,
  UNLIKE_POST,
  UPDATE_POST,
  DELETE_POST,
  EDIT_COMMENT,
  ADD_COMMENT,
  DELETE_COMMENT,
  GET_ALL_POSTS,
} from "../constatnts/postActionTypes";
import { GET_POST_ERRORS } from "../constatnts/errorActiontype";
export const getPosts = () => {
  return (dispatch) => {
    return axios
      .get(`${process.env.REACT_APP_API_URL}api/post/`)
      .then((res) => {
        dispatch({ type: GET_POSTES, payload: res.data });
      })
      .catch((err) => console.log(err));
  };
};
export const addPost = (data, idC) => {
  return (dispatch) => {
    return axios
      .post(`${process.env.REACT_APP_API_URL}api/post/${idC}`, data)
      .then((res) => {
        if (res.data.errors) {
          dispatch({ type: GET_POST_ERRORS, payload: res.data.errors });
        } else {
          dispatch({ type: GET_POST_ERRORS, payload: "" });
        }
      });
  };
};

export const updatePost = (postId, postMessage) => {
  return (dispatch) => {
    return axios({
      method: "put",
      url: `${process.env.REACT_APP_API_URL}api/post/${postId}`,
      data: { postMessage },
    })
      .then((res) => {
        dispatch({ type: UPDATE_POST, payload: { postMessage, postId } });
      })
      .catch((err) => console.log(err));
  };
};

